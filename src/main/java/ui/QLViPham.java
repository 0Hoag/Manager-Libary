private void search() {
        vpdao = new ViPhamDAO();
        String searchText = txtTimKiem.getText().trim();

        String sql = "SELECT * FROM ViPham WHERE MaDocGia LIKE ?";
        list = vpdao.selectBySql(sql, "%" + searchText + "%");
        tableModel.setRowCount(0);
        int index = 0;
        for (PhieuPhat p : list) {
            Object[] rows = new Object[]{
                ++index,
                p.getPhieuPhatId(),
                p.getMaDocGia().getDocGiaId(),
                p.getNoiDungPhat(),
                p.getPhiphat(),
                p.getNgayTao()
            };
            tableModel.addRow(rows);
        }

    }

    private PhieuPhat getForm() {
        dgDao = new DocGiaDAO();

        int maPhieuPhat;
        if (txtMaPhieuPhat.getText().isEmpty()) {
            maPhieuPhat = 0;
        } else {
            maPhieuPhat = Integer.parseInt(txtMaPhieuPhat.getText());
        }
        int maDocGia = Integer.parseInt(txtMaDocGia.getText());
        int phiPhat = Integer.parseInt(txtPhiPhat.getText());
        String noiDungPhat = txtaNoiDungPhat.getText();
        Date ngayTao = XDate.toDate(txtNgayTao.getText());

        DocGia dg = dgDao.selectById(maDocGia);
        PhieuPhat p = new PhieuPhat(maPhieuPhat, dg, noiDungPhat, phiPhat, ngayTao);
        return p;
    }

    private void setForm(int index) {
        PhieuPhat p = list.get(index);
        txtMaDocGia.setText(p.getMaDocGia().getDocGiaId() + "");
        txtaNoiDungPhat.setText(p.getNoiDungPhat());
        txtPhiPhat.setText(p.getPhiphat() + "");
        txtNgayTao.setText(p.getNgayTao() + "");
        txtMaPhieuPhat.setText(p.getPhieuPhatId() + "");
    }

    private void addViolation() {

        vpdao = new ViPhamDAO();
        try {

            if (XValidate.checkNullText(txtMaDocGia, txtaNoiDungPhat, txtPhiPhat)) {
                PhieuPhat p = getForm();
                vpdao.insert(p);
                XDialog.alert(this, "Taọ thành công");
            }

        } catch (Exception e) {
            XDialog.alert(this, e.getMessage());
        }

    }

    private void updateViolation() {
        vpdao = new ViPhamDAO();
        
        try {
            PhieuPhat p = getForm();
            vpdao.update(p);
            XDialog.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            XDialog.alert(this, "Cập nhật thất bại");
        }
    }

