    private void loadData() {
        vpdao = new ViPhamDAO();
        list = vpdao.selectAll();
        tableModel.setRowCount(0);
        int index = 0;
        for (PhieuPhat p : list) {
            Object[] rows = new Object[]{
                ++index,
                p.getPhieuPhatId(),
                p.getMaDocGia().getDocGiaId(),
                p.getNoiDungPhat(),
                p.getPhiphat(),
                p.getNgayTao()
            };
            tableModel.addRow(rows);
        }

    }

