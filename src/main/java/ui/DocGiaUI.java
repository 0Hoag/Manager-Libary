    private void changeTabs(JPanel p) {
        pnlHienThi.removeAll();
        pnlHienThi.add(p);
        pnlHienThi.repaint();
        pnlHienThi.revalidate();
    }

    private void init() {
        this.setLocationRelativeTo(null);
        DocGia dg = dgDAO.selectById(AccountDAO.getCurrentId());
        System.out.println(dg);
        lblId.setText(dg.getDocGiaId() + "");
        lblTen.setText(dg.getHoten() + "");
        
        DocGia_Sach dgs = new DocGia_Sach();
        changeTabs(dgs);
        TaskManager.getInstance().submitTask(() -> {
            dgs.loadTableSach();    
        });
    }

