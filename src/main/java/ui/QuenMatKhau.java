    private void recoveryPass() {
        Account ac = acDAO.selectByEmail(txtEmail.getText());
        if (XValidate.checkNullText(txtEmail)
                && XValidate.checkNullText(pwdPass)
                && XValidate.checkNullText(txtMaXacThuc)) {
            if (txtMaXacThuc.getText().equals(maCode)) {
                ac.setPass(new String(pwdPass.getPassword()));
                acDAO.update(ac);
                XDialog.alert(this, "Khôi phục mật khẩu thành công");
                clearForm();
            } else {
                XDialog.alert(this, "Mã xác thực không đúng");
            }

        }

    }

