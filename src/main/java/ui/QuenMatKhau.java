    private void sendMail(String to) {
        // Thông tin cấu hình cho kết nối SMTP
        String host = "smtp.gmail.com";
        final String user = "tienvtps40685@gmail.com"; // Thay thế bằng email của bạn
        final String password = "raft uzed uukz ltvo"; // Thay thế bằng mật khẩu của bạn

        // Thiết lập các thuộc tính SMTP
        Properties properties = new Properties();
        properties.put("mail.smtp.auth", "true");
        properties.put("mail.smtp.starttls.enable", "true");
        properties.put("mail.smtp.host", host);
        properties.put("mail.smtp.port", "587");

        // Tạo một phiên làm việc với thông tin xác thực
        Session session = Session.getInstance(properties, new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(user, password);
            }
        });

        try {

            // Tạo một tin nhắn email
            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(user));
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(to));
            message.setSubject("TẠO LẠI MẬT KHẨU CHO ỨNG DỤNG THƯ VIỆN");
            message.setText("Mã xác nhận của bạn là:" + maCode);

            // Gửi tin nhắn
            Transport.send(message);

        } catch (MessagingException e) {
            e.printStackTrace();
            System.out.println("Gửi email thất bại");
        }
    }

    private void startCountdown() {
        int[] seconds = {30}; // Biến seconds là một mảng
        new Timer(1000, e -> {
            if (seconds[0] > 0) {
                seconds[0]--;
                lblLayMa.setText(seconds[0] + "");
                lblLayMa.setEnabled(false);
            } else {
                ((Timer) e.getSource()).stop();
                lblLayMa.setText("Lấy mã");
                lblLayMa.setEnabled(true);
            }
        }).start();

    }

